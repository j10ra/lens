function LoginPage() {
  const [email, setEmail] = useState("");
  const [magicLinkSent, setMagicLinkSent] = useState(false);
  const [loading, setLoading] = useState<"github" | "google" | "magic" | null>(null);
  const [error, setError] = useState<string | null>(null);

  async function handleOAuth(provider: "github" | "google") {
    setLoading(provider);
    setError(null);
    const { error } = await supabase.auth.signInWithOAuth({
      provider,
      options: { redirectTo: window.location.origin + "/dashboard" },
    });
    if (error) {
      setError(error.message);
      setLoading(null);
    }
  }

  async function handleMagicLink(e: React.FormEvent) {
    e.preventDefault();
    if (!email.trim()) return;
    setLoading("magic");
    setError(null);
    const { error } = await supabase.auth.signInWithOtp({
      email: email.trim(),
      options: { emailRedirectTo: window.location.origin + "/dashboard" },
    });
    if (error) {
      setError(error.message);
    } else {
      setMagicLinkSent(true);
    }
    setLoading(null);
  }

  return (
    <div className="flex min-h-screen items-center justify-center px-4 bg-background">
      <div className="w-full max-w-sm">
        <div className="mb-8 text-center">
          <Link to="/" className="text-2xl font-bold tracking-tight">
            <span className="text-primary">LENS</span>
          </Link>
        </div>

        <div className="rounded-xl border bg-card p-8">
          <h1 className="mb-6 text-center text-xl font-semibold text-card-foreground">
            Sign in to LENS
          </h1>

          {error && (
            <div className="mb-4 rounded-lg border border-destructive/30 bg-destructive/10 px-4 py-2.5 text-sm text-destructive">
              {error}
            </div>
          )}

          <div className="space-y-3">
            <button
              onClick={() => handleOAuth("github")}
              disabled={loading !== null}
              className="flex w-full items-center justify-center gap-3 rounded-lg border bg-secondary px-4 py-3 text-sm font-medium text-secondary-foreground transition-colors hover:bg-accent disabled:opacity-50"
            >
              <GitHubIcon />
              {loading === "github" ? "Redirecting..." : "Continue with GitHub"}
            </button>
            <button
              onClick={() => handleOAuth("google")}
              disabled={loading !== null}
              className="flex w-full items-center justify-center gap-3 rounded-lg border bg-secondary px-4 py-3 text-sm font-medium text-secondary-foreground transition-colors hover:bg-accent disabled:opacity-50"
            >
              <GoogleIcon />
              {loading === "google" ? "Redirecting..." : "Continue with Google"}
            </button>
          </div>

          <div className="my-6 flex items-center gap-4">
            <div className="h-px flex-1 bg-border" />
            <span className="text-xs text-muted-foreground">or</span>
            <div className="h-px flex-1 bg-border" />
          </div>

          {magicLinkSent ? (
            <div className="rounded-lg border border-success/30 bg-success/10 px-4 py-4 text-center">
              <p className="text-sm font-medium text-success">Check your email</p>
              <p className="mt-1 text-xs text-muted-foreground">
                We sent a sign-in link to{" "}
                <span className="text-foreground">{email}</span>
              </p>
              <button
                onClick={() => { setMagicLinkSent(false); setEmail(""); }}
                className="mt-3 text-xs text-muted-foreground transition-colors hover:text-foreground"
              >
                Use a different email
              </button>
            </div>
          ) : (
            <form onSubmit={handleMagicLink} className="space-y-3">
              <input
                type="email"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                placeholder="you@example.com"
                className="w-full rounded-lg border bg-background px-4 py-3 text-sm text-foreground placeholder-muted-foreground outline-none transition-colors focus:border-ring focus:ring-1 focus:ring-ring"
              />
              <button
                type="submit"
                disabled={loading !== null}
                className="w-full rounded-lg bg-primary px-4 py-3 text-sm font-semibold text-primary-foreground transition-colors hover:bg-primary/90 disabled:opacity-50"
              >
                {loading === "magic" ? "Sending..." : "Send Magic Link"}
              </button>
            </form>
          )}
        </div>

        <p className="mt-6 text-center text-xs text-muted-foreground">
          By signing in, you agree to our{" "}
          <a href="https://lens-engine.com/terms" className="text-primary hover:underline">Terms</a>
          {" "}and{" "}
          <a href="https://lens-engine.com/privacy" className="text-primary hover:underline">Privacy Policy</a>.
        </p>
      </div>
    </div>
  );
}
